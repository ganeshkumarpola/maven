package Encapsulstion;

public class Car {
	
	private int cost;
	private String color;
	private String model;
	
	public void setCost(int cost) {
		
		this.cost=cost;
	}
	
	public void setColor(String color) {
		
		this.color=color;
	}
	public void setModel(String model) {
		
		this.color=model;
	}
	
	public int getCost() {
		
		return this.cost;
	}
	
	public String getColor() {
		
		return this.color;
	}
	
	public String getModel() {
		
		return this.model;
	}

}
